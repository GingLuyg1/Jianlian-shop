create table if not exists public.promotion_visits (
  id bigint generated by default as identity primary key,
  inviter_id uuid not null references public.profiles(id) on delete cascade,
  visitor_key text not null,
  created_at timestamptz not null default now(),
  unique (inviter_id, visitor_key)
);

create index if not exists promotion_visits_inviter_idx
on public.promotion_visits(inviter_id);

alter table public.promotion_visits enable row level security;

create table if not exists public.promotion_commissions (
  id uuid primary key default gen_random_uuid(),
  inviter_id uuid not null references public.profiles(id) on delete cascade,
  referred_user_id uuid references public.profiles(id) on delete set null,
  referred_user_label text,
  paid_at timestamptz not null default now(),
  recharge_amount numeric(12, 2) not null default 0,
  commission_amount numeric(12, 2) not null default 0,
  status text not null default 'confirmed'
    check (status in ('pending', 'confirmed', 'rejected')),
  created_at timestamptz not null default now()
);

create index if not exists promotion_commissions_inviter_idx
on public.promotion_commissions(inviter_id, paid_at desc);

alter table public.promotion_commissions enable row level security;

create table if not exists public.promotion_withdrawals (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete cascade,
  amount numeric(12, 2) not null,
  status text not null default 'pending'
    check (status in ('pending', 'approved', 'rejected')),
  created_at timestamptz not null default now()
);

create index if not exists promotion_withdrawals_user_idx
on public.promotion_withdrawals(user_id, created_at desc);

alter table public.promotion_withdrawals enable row level security;

create or replace function public.record_promotion_visit(
  input_invite_code text,
  input_visitor_key text
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  inviter_profile_id uuid;
begin
  if nullif(trim(input_invite_code), '') is null
    or nullif(trim(input_visitor_key), '') is null then
    return;
  end if;

  select id into inviter_profile_id
  from public.profiles
  where lower(invite_code) = lower(trim(input_invite_code))
  limit 1;

  if inviter_profile_id is null then
    return;
  end if;

  insert into public.promotion_visits (inviter_id, visitor_key)
  values (inviter_profile_id, input_visitor_key)
  on conflict (inviter_id, visitor_key) do nothing;
end;
$$;

grant execute on function public.record_promotion_visit(text, text)
to anon, authenticated;

drop policy if exists "Users can view own profile" on public.profiles;
create policy "Users can view own profile"
on public.profiles for select
to authenticated
using (auth.uid() = id);

drop policy if exists "Users can insert own profile" on public.profiles;
create policy "Users can insert own profile"
on public.profiles for insert
to authenticated
with check (
  auth.uid() = id
  and email = auth.jwt()->>'email'
  and role = public.role_for_email(auth.jwt()->>'email')
);

drop policy if exists "Users can update own non-role profile" on public.profiles;
create policy "Users can update own non-role profile"
on public.profiles for update
to authenticated
using (auth.uid() = id)
with check (auth.uid() = id and role = public.current_user_role(auth.uid()));

drop policy if exists "Users can view own promotion visits" on public.promotion_visits;
create policy "Users can view own promotion visits"
on public.promotion_visits for select
to authenticated
using (auth.uid() = inviter_id);

drop policy if exists "Users can view own promotion commissions" on public.promotion_commissions;
create policy "Users can view own promotion commissions"
on public.promotion_commissions for select
to authenticated
using (auth.uid() = inviter_id);

drop policy if exists "Users can create own withdrawal requests" on public.promotion_withdrawals;
