-- Run in Supabase SQL Editor.

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  phone text,
  role text not null default 'user'
    check (role in ('user', 'admin', 'support', 'finance')),
  balance numeric(12, 2) not null default 0,
  promotion_balance numeric(12, 2) not null default 0,
  invite_code text unique,
  referred_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.profiles
add column if not exists promotion_balance numeric(12, 2) not null default 0;

alter table public.profiles
add column if not exists invite_code text;

alter table public.profiles
add column if not exists referred_by uuid references public.profiles(id) on delete set null;

create index if not exists profiles_role_idx on public.profiles(role);
create index if not exists profiles_email_idx on public.profiles(email);
create unique index if not exists profiles_invite_code_unique_idx
on public.profiles(invite_code)
where invite_code is not null;
create index if not exists profiles_invite_code_idx on public.profiles(invite_code);
create index if not exists profiles_referred_by_idx on public.profiles(referred_by);

alter table public.profiles enable row level security;

create or replace function public.role_for_email(user_email text)
returns text
language sql
immutable
as $$
  select case
    when lower(coalesce(user_email, '')) = 'gac000189@gmail.com' then 'admin'
    else 'user'
  end
$$;

create or replace function public.current_user_role(user_id uuid)
returns text
language sql
security definer
set search_path = public
stable
as $$
  select role from public.profiles where id = user_id
$$;

create or replace function public.is_admin(user_id uuid)
returns boolean
language sql
security definer
set search_path = public
stable
as $$
  select exists (
    select 1 from public.profiles
    where id = user_id and role = 'admin'
  )
$$;

create or replace function public.invite_code_for_user(user_id uuid)
returns text
language sql
immutable
as $$
  select 'JL' || upper(substr(md5(user_id::text), 1, 6))
$$;

create or replace function public.handle_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists profiles_updated_at on public.profiles;
create trigger profiles_updated_at
before update on public.profiles
for each row execute function public.handle_updated_at();

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  inviter_profile_id uuid;
  submitted_invite_code text;
begin
  submitted_invite_code := nullif(trim(new.raw_user_meta_data->>'invite_code'), '');

  if submitted_invite_code is not null then
    select id into inviter_profile_id
    from public.profiles
    where lower(invite_code) = lower(submitted_invite_code)
      and id <> new.id
    limit 1;
  end if;

  insert into public.profiles (
    id,
    email,
    phone,
    role,
    invite_code,
    referred_by
  )
  values (
    new.id,
    lower(new.email),
    new.phone,
    public.role_for_email(new.email),
    public.invite_code_for_user(new.id),
    inviter_profile_id
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

